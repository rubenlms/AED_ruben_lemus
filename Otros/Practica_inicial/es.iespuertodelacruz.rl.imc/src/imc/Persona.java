package imc;

/**
 *
 * @author dama
 */
public class Persona {
    
    String nombre;
    String apellidos;
    int edad;
    int altura;
    int peso;

    public Persona() {
    }

    public Persona(String nombre, String apellidos, int edad, int altura, int peso) {
        this.nombre = nombre;
        this.apellidos = apellidos;
        this.edad = edad;
        this.altura = altura;
        this.peso = peso;
    }
    
    
    
}
